CREATE TABLE games (
    appid                       INTEGER PRIMARY KEY NOT NULL,
    name                        TEXT,
    release_date                TEXT NOT NULL,
    estimated_owners            TEXT NOT NULL,
    peak_ccu                    INTEGER NOT NULL,
    required_age                INTEGER NOT NULL,
    price                       NUMERIC NOT NULL,
    dlc_count                   INTEGER NOT NULL,
    about_the_game              TEXT,
    supported_languages         TEXT, 
    full_audio_languages        TEXT, 
    reviews                     TEXT,
    header_image                TEXT NOT NULL,
    website                     TEXT,
    support_url                 TEXT,
    support_email               TEXT,
    windows                     BOOLEAN,
    mac                         BOOLEAN,
    linux                       BOOLEAN,
    metacritic_score            INTEGER,
    metacritic_url              TEXT,
    user_score                  INTEGER,
    positive                    INTEGER NOT NULL,
    negative                    INTEGER NOT NULL,
    score_rank                  TEXT,
    achievements                INTEGER NOT NULL,
    recommendations             INTEGER NOT NULL,
    notes                       TEXT,
    average_playtime_forever    INTEGER NOT NULL,
    average_playtime_2weeks     INTEGER NOT NULL,
    median_playtime_forever     INTEGER NOT NULL,
    median_playtime_2weeks      INTEGER NOT NULL,
    developers                  TEXT,   
    publishers                  TEXT,
    categories                  TEXT,
    genres                      TEXT,
    tags                        TEXT,
    screenshots                 TEXT,         
    movies                      TEXT          
);